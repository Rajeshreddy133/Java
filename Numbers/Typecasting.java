public class Typecasting
{
    public static void main(String args[])
    {
        int a =300;
        byte k=(byte)a;
        float f=4.6f;
        int x=(int)f;
        float c=(float)a;
        float b=a+f;
        int y=k+a;
        System.out.println(k);
        System.out.println(x);
        System.out.println(c);
        System.out.println(b);
        System.out.println(y);
    }
}